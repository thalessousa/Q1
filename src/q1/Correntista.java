package q1;

/**
 *
 * @author thale
 */
public class Correntista {

    private String nome;
    private double cpf;

    public Correntista() {
        this.nome = nome;
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public double getCpf() {
        return cpf;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setCpf(double cpf) {
        this.cpf = cpf;
    }

}
